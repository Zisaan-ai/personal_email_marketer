def inject_accordion(match):
    full_card = match.group(0)
    title_match = re.search(r'<h3 class="about-title">(.*?)</h3>', full_card)
    if not title_match: return full_card
    title = title_match.group(1).strip()
    
    guide = guides.get(title)
    if not guide: return full_card
    
    accordion_html = f'''
                    <div style="margin-top: auto; padding-top: 16px;">
                        <div onclick="const content = this.nextElementSibling; const icon = this.querySelector('.htu-icon'); if(content.style.maxHeight){{content.style.maxHeight = null; icon.style.transform = 'rotate(0deg)'; content.style.marginTop = '0'; content.style.opacity = '0';}}else{{content.style.maxHeight = content.scrollHeight + 'px'; icon.style.transform = 'rotate(180deg)'; content.style.marginTop = '12px'; content.style.opacity = '1';}}" style="display:flex; align-items:center; justify-content:space-between; padding: 12px 16px; background: rgba(var(--icon-color-rgb), 0.1); border-radius: 12px; cursor: pointer; transition: all 0.3s; border: 1px solid rgba(var(--icon-color-rgb), 0.2);">
                            <div style="display:flex; align-items:center; gap: 8px; color: var(--icon-color-1); font-weight: 700; font-size: 14px;">
                                <i class="fa-solid fa-circle-question"></i> How to Use
                            </div>
                            <div class="htu-icon" style="color: var(--icon-color-1); font-size: 14px; transition: transform 0.3s ease;">
                                <i class="fa-solid fa-chevron-down"></i>
                            </div>
                        </div>
                        <div style="max-height: 0; overflow: hidden; transition: all 0.4s cubic-bezier(0.4, 0, 0.2, 1); opacity: 0; margin-top: 0;">
                            <div style="padding: 16px; background: #f8fafc; border-radius: 12px; border: 1px solid #e2e8f0; font-size: 13.5px; color: #475569; line-height: 1.6;">
                                <strong>Guide:</strong> {guide['en']}<br><br>
                                <strong>????:</strong> {guide['bn']}
                            </div>
                        </div>
                    </div>'''
    
    # Insert right before the last </div> of the card
    last_div_idx = full_card.rfind('</div>')
    new_card = full_card[:last_div_idx] + accordion_html + '\n                ' + full_card[last_div_idx:]
    return new_card

